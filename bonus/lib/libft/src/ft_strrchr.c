#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	*ret;

	ret = NULL;
	i = 0;
	while (s[i])
	{
		if (s[i] == (char)c)
			ret = &((char *)s)[i];
		i++;
	}
	if (s[i] == (char)c)
		ret = &((char *)s)[i];
	if (c == '\0')
		ret = &((char *)s)[i];
	return (ret);
}
